import SwiftUI

struct CustomTeamListView: View {
    @StateObject private var viewModel = CustomTeamListViewModel()

    var body: some View {
        ScrollView {
            VStack(spacing: 16) {
                ForEach(viewModel.teams) { team in
                    CustomTeamCardView(viewModel: team)
                        .padding(.all)
                }
            }
        }
    }
}


